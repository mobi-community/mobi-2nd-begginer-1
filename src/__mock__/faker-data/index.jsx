const randomPhoneNumber = () => {
    return `010-${Math.floor(Math.random() * 9999) + 1000}-${Math.floor(Math.random() * 9999) + 1000}`;
};

const blindPhoneNumber = () => {
    const blindPhoneNum = randomPhoneNumber().split('-');
    //[010,1234,1234] 쪼개기
    blindPhoneNum[1] = '****';
    //[010,****,1234] 가리기
    return blindPhoneNum.join('-');
    //010-****-1234 붙이기
};

